vector<pair<ll, ll>> fact(ll n) {
    vector<pair<ll, ll>> ret;
    for (ll div = 2; div * div <= n; div++) {
        if (n % div != 0)continue;
        ll exp = 0;
        while (n % div == 0) {
            exp++;
            n /= div;
        }
        ret.push_back(make_pair(div, exp));
    }
    if (n != 1)ret.push_back(make_pair(n, 1));
    return ret;
}

int main() {
    int N;
    cin>>N;

    map<ll,int> exp;
    for(int i=2;i<=N;i++){
        auto pf=fact(i);
        for(auto ff:pf){
            int pp=ff.first,e=ff.second;
            exp[pp]+=e;
        }
    }

    ll ans=1;
    for(auto e:exp){
        ans*=(e.second+1);
        ans%=MOD;
    }
    cout<<ans<<endl;
}